void MatrixDotDisplay_closeStrobe() {
    output_high(PIN_STROBE); // transfiere los datos a las salidas del registro 4094
    output_low(PIN_STROBE); // un pulso de strobe 
}

void MatrixDotDisplay_shift(int32 &dato1) {
    int contador;
    for (contador = 0; contador < 8; contador++) {

        output_bit(PIN_DATA, (!(dato1 & 0x80000000)));
        output_high(PIN_CLOCK);
        output_low(PIN_CLOCK);
        dato1 <<= 1;
    }
}

void MatrixDotDisplay_desplegar(int32 x1, int32 x2, int32 x3, int32 x4) {

    char contador2 = 10;
    for (contador2 = 0; contador2 < 4; contador2++) {

        MatrixDotDisplay_shift(x4);
        MatrixDotDisplay_shift(x3);
        MatrixDotDisplay_shift(x2);
        MatrixDotDisplay_shift(x1);
    }
}

void MatrixDotDisplay_sendDataToMatrixDotDisplay(int32 data[], int numberOfDisplays, int bright) {
    int brightness[10] = {10, 34, 58, 82, 106, 130, 154, 178, 202, 255};
    int i;
    int j;
    for (i = 0; i < 4; i++) {

        for (j = 0; j < numberOfDisplays; j++) {
            MatrixDotDisplay_desplegar(
                    data[i + 16 * j],
                    data[i + 4 + 16 * j],
                    data[i + 8 + 16 * j],
                    data[i + 12 + 16 * j]
                    );
        }
        //        output_low(PIN_OE);
        set_pwm1_duty(0);

        switch (i) {
            case 0:
                output_low(PIN_A);
                output_low(PIN_B);

                break;

            case 1:
                output_high(PIN_A);
                output_low(PIN_B);

                break;

            case 2:
                output_low(PIN_A);
                output_high(PIN_B);

                break;

            case 3:
                output_high(PIN_A);
                output_high(PIN_B);

                break;
        }
        MatrixDotDisplay_closeStrobe();
        //        output_high(PIN_OE);
        set_pwm1_duty(brightness[bright]);

    }
}
